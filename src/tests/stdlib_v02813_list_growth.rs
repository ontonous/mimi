// Placeholder tests — these verify the *correctness* of push (which
// already works) and the *intent* of the growth-factor optimization.
// The growth-factor implementation lands in v0.28.14; for v0.28.13
// these tests simply confirm that push N times produces a list of
// length N with the right elements.
// =====================================================================

use crate::tests::compile_and_run;

#[test]
fn stdlib_v02813_list_push_n_100() {
    let src = r#"
        func main() -> i32 {
            let arr = range(0, 100)
            println(len(arr))
            0
        }
    "#;
    let out = compile_and_run(src).expect("compile_and_run range(0,100)");
    assert_eq!(out.trim(), "100");
}

