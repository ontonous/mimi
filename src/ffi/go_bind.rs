//! Go FFI binding generator (CGO) from Mimi extern declarations.
//!
//! Generates a `.go` file with `import "C"` and CGO directives.
//!
//! Usage:
//!   mimi emit-go-bindings path/to/pkg.mimi -o bindings.go

use std::collections::HashMap;
use std::fmt::Write;

use crate::ast::{ExternFunc, Type, TypeAttribute, TypeDef, TypeDefKind};
use crate::ffi::contract::{FfiArgContract, FfiContract};

/// Go binding generator — produces a CGO-compatible `.go` file.
pub struct GoBindGenerator {
    type_defs: HashMap<String, TypeDef>,
    module_name: String,
}

impl GoBindGenerator {
    pub fn new(type_defs: HashMap<String, TypeDef>, module_name: &str) -> Self {
        Self {
            type_defs,
            module_name: module_name.to_string(),
        }
    }

    /// Generate the complete Go CGO bindings source.
    pub fn generate(&self, extern_funcs: &[ExternFunc]) -> Result<String, std::fmt::Error> {
        let mut out = String::new();

        writeln!(out, "// Auto-generated by Mimi FFI binding generator")?;
        writeln!(out, "// Module: {}", self.module_name)?;
        writeln!(out, "// Do not edit manually.")?;
        writeln!(out)?;
        writeln!(out, "package {}", sanitize_go_package(&self.module_name))?;
        writeln!(out)?;
        writeln!(out, "/*")?;
        writeln!(out, "#cgo LDFLAGS: -lmimi_runtime -lpthread -ldl -lm")?;
        writeln!(out, "#include <stdlib.h>")?;
        writeln!(out, "#include <stdbool.h>")?;
        writeln!(out, "extern void mimi_string_free(char* s);")?;

        // C struct definitions for #[repr(C)] records
        self.write_c_struct_decls(&mut out)?;

        // Extern function declarations
        for func in extern_funcs {
            let contract = self.build_contract(func);
            self.write_c_decl(&mut out, func, &contract)?;
        }
        writeln!(out, "*/")?;
        writeln!(out, "import \"C\"")?;
        writeln!(out, "import \"unsafe\"")?;
        writeln!(out)?;

        // Go struct types for #[repr(C)] records
        self.write_go_struct_defs(&mut out)?;

        // Go callback types, slots, and C-exported trampolines
        self.write_go_callback_support(&mut out, extern_funcs)?;

        for func in extern_funcs {
            let contract = self.build_contract(func);
            self.write_go_func(&mut out, func, &contract)?;
        }

        Ok(out)
    }

    fn build_contract(&self, func: &ExternFunc) -> FfiContract {
        let record_type_names: std::collections::HashSet<String> = self
            .type_defs
            .iter()
            .filter(|(_, td)| matches!(td.kind, TypeDefKind::Record(_)))
            .map(|(name, _)| name.clone())
            .collect();
        let repr_c_record_names: std::collections::HashSet<String> = self
            .type_defs
            .iter()
            .filter(|(_, td)| td.attributes.contains(&TypeAttribute::ReprC))
            .map(|(name, _)| name.clone())
            .collect();
        FfiContract::from_extern_with_caps_repr(
            func,
            &std::collections::HashSet::new(),
            &record_type_names,
            &repr_c_record_names,
        )
    }

    fn write_c_struct_decls(&self, out: &mut String) -> Result<(), std::fmt::Error> {
        let mut repr_c: Vec<(&String, &TypeDef)> = self
            .type_defs
            .iter()
            .filter(|(_, td)| td.attributes.contains(&TypeAttribute::ReprC))
            .collect();
        repr_c.sort_by_key(|(name, _)| *name);
        for (name, td) in repr_c {
            if let TypeDefKind::Record(fields) = &td.kind {
                writeln!(out, "typedef struct {0} {{", name)?;
                for field in fields {
                    let c_type = self.mimi_type_to_c_field(&field.ty);
                    writeln!(out, "    {} {};", c_type, field.name)?;
                }
                writeln!(out, "}} {};", name)?;
            }
        }
        Ok(())
    }

    fn write_go_struct_defs(&self, out: &mut String) -> Result<(), std::fmt::Error> {
        let mut repr_c: Vec<(&String, &TypeDef)> = self
            .type_defs
            .iter()
            .filter(|(_, td)| td.attributes.contains(&TypeAttribute::ReprC))
            .collect();
        repr_c.sort_by_key(|(name, _)| *name);
        if !repr_c.is_empty() {
            writeln!(out, "// Go structs matching #[repr(C)] Mimi records")?;
            for (name, td) in repr_c {
                if let TypeDefKind::Record(fields) = &td.kind {
                    writeln!(out, "type {} struct {{", name)?;
                    for field in fields {
                        let go_type = self.mimi_type_to_go_field(&field.ty);
                        let go_name = capitalize(&field.name);
                        writeln!(out, "    {} {}", go_name, go_type)?;
                    }
                    writeln!(out, "}}")?;
                    writeln!(out)?;
                }
            }
        }
        Ok(())
    }

    fn write_go_callback_support(
        &self,
        out: &mut String,
        extern_funcs: &[ExternFunc],
    ) -> Result<(), std::fmt::Error> {
        // Generate type aliases and slots for callback parameters
        for func in extern_funcs {
            let contract = self.build_contract(func);
            for (i, p) in func.params.iter().enumerate() {
                if let FfiArgContract::Callback {
                    param_types,
                    ret_type,
                } = &contract.args[i]
                {
                    let go_type = self.callback_go_type(param_types, ret_type);
                    let slot = self.callback_slot_name(&func.name, &p.name);
                    writeln!(
                        out,
                        "type {} {}",
                        self.callback_type_name(&func.name, &p.name),
                        go_type
                    )?;
                    writeln!(
                        out,
                        "var {} {}",
                        slot,
                        self.callback_type_name(&func.name, &p.name)
                    )?;
                }
            }
        }
        writeln!(out)?;
        for func in extern_funcs {
            let contract = self.build_contract(func);
            for (i, p) in func.params.iter().enumerate() {
                if let FfiArgContract::Callback {
                    param_types,
                    ret_type,
                } = &contract.args[i]
                {
                    self.write_go_callback_trampoline(
                        out,
                        &func.name,
                        &p.name,
                        param_types,
                        ret_type,
                    )?;
                }
            }
        }
        Ok(())
    }

    fn write_go_callback_trampoline(
        &self,
        out: &mut String,
        func_name: &str,
        param_name: &str,
        param_types: &[Type],
        ret_type: &Type,
    ) -> Result<(), std::fmt::Error> {
        let tramp = self.callback_trampoline_name(func_name, param_name);
        writeln!(out, "//export {}", tramp)?;
        let c_args: Vec<String> = param_types
            .iter()
            .enumerate()
            .map(|(j, ty)| format!("arg{} {}", j, self.go_cgo_param_type(ty)))
            .collect();
        let c_ret = self.go_cgo_ret_type(ret_type);
        let slot = self.callback_slot_name(func_name, param_name);
        let go_args: Vec<String> = param_types
            .iter()
            .enumerate()
            .map(|(j, ty)| format!("{}(arg{})", self.go_cast_from_c(ty), j))
            .collect();
        let call = format!("{}({})", slot, go_args.join(", "));
        if c_ret.is_empty() {
            writeln!(out, "func {}({}) {{", tramp, c_args.join(", "))?;
            writeln!(out, "    if {} == nil {{ return }}", slot)?;
            writeln!(out, "    {}", call)?;
            writeln!(out, "}}")?;
        } else {
            writeln!(out, "func {}({}) {} {{", tramp, c_args.join(", "), c_ret)?;
            writeln!(
                out,
                "    if {} == nil {{ return {} }}",
                slot,
                self.go_callback_default_ret(ret_type)
            )?;
            writeln!(out, "    return {}({})", self.go_cast_to_c(ret_type), call)?;
            writeln!(out, "}}")?;
        }
        writeln!(out)?;
        Ok(())
    }

    fn callback_type_name(&self, func_name: &str, param_name: &str) -> String {
        go_export_name(&format!("{}_{}_cb", func_name, param_name))
    }

    fn callback_slot_name(&self, func_name: &str, param_name: &str) -> String {
        format!("{}_{}_cb_slot", func_name, param_name)
    }

    fn callback_trampoline_name(&self, func_name: &str, param_name: &str) -> String {
        format!("mimi_cb_{}_{}", func_name, param_name)
    }

    fn callback_go_type(&self, param_types: &[Type], ret_type: &Type) -> String {
        let args: Vec<String> = param_types
            .iter()
            .map(|ty| self.go_callback_arg_type(ty))
            .collect();
        let ret = self.go_callback_ret_type(ret_type);
        if ret.is_empty() {
            format!("func({})", args.join(", "))
        } else {
            format!("func({}) {}", args.join(", "), ret)
        }
    }

    fn callback_c_type(&self, param_types: &[Type], ret_type: &Type) -> String {
        self.callback_c_param_decl("callback", param_types, ret_type)
    }

    fn callback_c_param_decl(&self, name: &str, param_types: &[Type], ret_type: &Type) -> String {
        let args: Vec<String> = param_types
            .iter()
            .map(|ty| self.c_callback_arg_type(ty))
            .collect();
        let ret = self.c_callback_ret_type(ret_type);
        let args_str = if args.is_empty() {
            "void".to_string()
        } else {
            args.join(", ")
        };
        format!("{} (*{})({})", ret, name, args_str)
    }

    fn c_callback_arg_type(&self, ty: &Type) -> String {
        // Match the original Mimi scalar widths in the C trampoline ABI.
        match ty.unlocated() {
            Type::Name(name, _) if name == "i32" => "int".to_string(),
            Type::Name(name, _) if name == "i64" => "long long".to_string(),
            Type::Name(name, _) if name == "f64" => "double".to_string(),
            Type::Name(name, _) if name == "bool" => "_Bool".to_string(),
            _ => "long long".to_string(),
        }
    }

    fn c_callback_ret_type(&self, ty: &Type) -> String {
        match ty.unlocated() {
            Type::Name(name, _) if name == "i32" => "int".to_string(),
            Type::Name(name, _) if name == "i64" => "long long".to_string(),
            Type::Name(name, _) if name == "f64" => "double".to_string(),
            Type::Name(name, _) if name == "bool" => "_Bool".to_string(),
            Type::Name(name, _) if name == "unit" => "void".to_string(),
            _ => "long long".to_string(),
        }
    }

    fn go_callback_arg_type(&self, ty: &Type) -> String {
        match ty.unlocated() {
            Type::Name(name, _) if name == "i32" => "int32".to_string(),
            Type::Name(name, _) if name == "i64" => "int64".to_string(),
            Type::Name(name, _) if name == "f64" => "float64".to_string(),
            Type::Name(name, _) if name == "bool" => "bool".to_string(),
            _ => "int64".to_string(),
        }
    }

    fn go_callback_ret_type(&self, ty: &Type) -> String {
        match ty.unlocated() {
            Type::Name(name, _) if name == "i32" => "int32".to_string(),
            Type::Name(name, _) if name == "i64" => "int64".to_string(),
            Type::Name(name, _) if name == "f64" => "float64".to_string(),
            Type::Name(name, _) if name == "bool" => "bool".to_string(),
            Type::Name(name, _) if name == "unit" => "".to_string(),
            _ => "int64".to_string(),
        }
    }

    fn go_cgo_param_type(&self, ty: &Type) -> String {
        match ty.unlocated() {
            Type::Name(name, _) if name == "i32" => "C.int".to_string(),
            Type::Name(name, _) if name == "i64" => "C.longlong".to_string(),
            Type::Name(name, _) if name == "f64" => "C.double".to_string(),
            Type::Name(name, _) if name == "bool" => "C._Bool".to_string(),
            _ => "C.longlong".to_string(),
        }
    }

    fn go_cgo_ret_type(&self, ty: &Type) -> String {
        match ty.unlocated() {
            Type::Name(name, _) if name == "i32" => "C.int".to_string(),
            Type::Name(name, _) if name == "i64" => "C.longlong".to_string(),
            Type::Name(name, _) if name == "f64" => "C.double".to_string(),
            Type::Name(name, _) if name == "bool" => "C._Bool".to_string(),
            Type::Name(name, _) if name == "unit" => "".to_string(),
            _ => "C.longlong".to_string(),
        }
    }

    fn go_cast_from_c(&self, ty: &Type) -> String {
        match ty.unlocated() {
            Type::Name(name, _) if name == "f64" => "float64".to_string(),
            Type::Name(name, _) if name == "bool" => "bool".to_string(),
            _ => "int64".to_string(),
        }
    }

    fn go_cast_to_c(&self, ty: &Type) -> String {
        match ty.unlocated() {
            Type::Name(name, _) if name == "f64" => "C.double".to_string(),
            Type::Name(name, _) if name == "bool" => "C._Bool".to_string(),
            _ => "C.longlong".to_string(),
        }
    }

    fn go_callback_default_ret(&self, ty: &Type) -> String {
        match ty.unlocated() {
            Type::Name(name, _) if name == "f64" => "0.0".to_string(),
            _ => "0".to_string(),
        }
    }

    fn write_c_decl(
        &self,
        out: &mut String,
        func: &ExternFunc,
        contract: &FfiContract,
    ) -> Result<(), std::fmt::Error> {
        let params: Vec<String> = func
            .params
            .iter()
            .enumerate()
            .map(|(i, p)| {
                if let FfiArgContract::Callback {
                    param_types,
                    ret_type,
                } = &contract.args[i]
                {
                    self.callback_c_param_decl(&p.name, param_types, ret_type)
                } else {
                    format!("{} {}", self.mimi_type_to_c(contract, i), p.name)
                }
            })
            .collect();
        let ret = self.ret_type_to_c(contract);
        writeln!(out, "extern {} {}({});", ret, func.name, params.join(", "))
    }

    fn write_go_func(
        &self,
        out: &mut String,
        func: &ExternFunc,
        contract: &FfiContract,
    ) -> Result<(), std::fmt::Error> {
        let go_params: Vec<String> = func
            .params
            .iter()
            .enumerate()
            .map(|(i, p)| {
                let ty = if let FfiArgContract::Callback { .. } = &contract.args[i] {
                    self.callback_type_name(&func.name, &p.name)
                } else {
                    self.mimi_type_to_go(contract, i)
                };
                format!("{} {}", p.name, ty)
            })
            .collect();
        let go_ret = self.ret_type_to_go(contract);

        // Build C call arguments with conversions
        let mut conversions = String::new();
        let mut c_args = Vec::new();
        for (i, p) in func.params.iter().enumerate() {
            match &contract.args[i] {
                FfiArgContract::StringBorrow
                | FfiArgContract::StringTransfer
                | FfiArgContract::Json => {
                    writeln!(conversions, "\t{}_c := C.CString({})", p.name, p.name)?;
                    writeln!(conversions, "\tdefer C.free(unsafe.Pointer({}_c))", p.name)?;
                    c_args.push(format!("{}_c", p.name));
                }
                FfiArgContract::StructByValue(name) => {
                    writeln!(conversions, "\t{}_c := C.{}{{}}", p.name, name)?;
                    if let Some(td) = self.type_defs.get(name) {
                        if let TypeDefKind::Record(fields) = &td.kind {
                            for field in fields {
                                let go_field = capitalize(&field.name);
                                writeln!(
                                    conversions,
                                    "\t{}_c.{} = C.{}({}.{})",
                                    p.name,
                                    field.name,
                                    self.mimi_type_to_c_field(&field.ty),
                                    p.name,
                                    go_field
                                )?;
                            }
                        }
                    }
                    c_args.push(format!("{}_c", p.name));
                }
                FfiArgContract::Int(scalar) => {
                    let cast = match scalar {
                        crate::ffi::contract::FfiScalarType::I32 => "C.int",
                        crate::ffi::contract::FfiScalarType::I64 => "C.longlong",
                        crate::ffi::contract::FfiScalarType::Bool => "C._Bool",
                    };
                    c_args.push(format!("{}({})", cast, p.name));
                }
                FfiArgContract::Cap(_)
                | FfiArgContract::CShared(_)
                | FfiArgContract::CBorrow(_)
                | FfiArgContract::CBorrowMut(_) => {
                    c_args.push(format!("C.longlong({})", p.name));
                }
                FfiArgContract::Float => {
                    c_args.push(format!("C.double({})", p.name));
                }
                FfiArgContract::Callback { .. } => {
                    let slot = self.callback_slot_name(&func.name, &p.name);
                    let tramp = self.callback_trampoline_name(&func.name, &p.name);
                    let type_name = self.callback_type_name(&func.name, &p.name);
                    writeln!(conversions, "\t{} = ({})({})", slot, type_name, p.name)?;
                    writeln!(conversions, "\tdefer func() {{ {} = nil }}()", slot)?;
                    c_args.push(format!("C.{}", tramp));
                }
                _ => {
                    c_args.push(p.name.clone());
                }
            }
        }

        let go_func_name = go_export_name(&func.name);
        writeln!(
            out,
            "func {}({}) {} {{",
            go_func_name,
            go_params.join(", "),
            go_ret
        )?;
        write!(out, "{}", conversions)?;
        match &contract.ret {
            crate::ffi::contract::FfiRetContract::String
            | crate::ffi::contract::FfiRetContract::StringOwned => {
                writeln!(out, "\tresult := C.{}({})", func.name, c_args.join(", "))?;
                writeln!(out, "\tdefer C.mimi_string_free(result)")?;
                writeln!(out, "\treturn C.GoString(result)")?;
            }
            crate::ffi::contract::FfiRetContract::Unit => {
                writeln!(out, "\tC.{}({})", func.name, c_args.join(", "))?;
            }
            _ => {
                writeln!(out, "\treturn C.{}({})", func.name, c_args.join(", "))?;
            }
        }
        writeln!(out, "}}")?;
        writeln!(out)
    }

    fn mimi_type_to_c(&self, contract: &FfiContract, index: usize) -> String {
        if index >= contract.args.len() {
            return "void*".to_string();
        }
        match &contract.args[index] {
            FfiArgContract::Int(scalar) => match scalar {
                crate::ffi::contract::FfiScalarType::I32 => "int".to_string(),
                crate::ffi::contract::FfiScalarType::I64 => "long long".to_string(),
                crate::ffi::contract::FfiScalarType::Bool => "_Bool".to_string(),
            },
            FfiArgContract::Float => "double".to_string(),
            FfiArgContract::StringBorrow | FfiArgContract::StringTransfer => "char*".to_string(),
            FfiArgContract::Cap(_) => "long long".to_string(),
            FfiArgContract::RawPtr(_) | FfiArgContract::RawPtrMut(_) => "void*".to_string(),
            FfiArgContract::CShared(_)
            | FfiArgContract::CBorrow(_)
            | FfiArgContract::CBorrowMut(_) => "long long".to_string(),
            FfiArgContract::Json => "char*".to_string(),
            FfiArgContract::StructByValue(name) => format!("struct {}", name),
            FfiArgContract::Callback {
                param_types,
                ret_type,
            } => self.callback_c_type(param_types, ret_type),
            FfiArgContract::Unsupported(_) => "void*".to_string(),
        }
    }

    fn ret_type_to_c(&self, contract: &FfiContract) -> String {
        match &contract.ret {
            crate::ffi::contract::FfiRetContract::Unit => "void".to_string(),
            crate::ffi::contract::FfiRetContract::Int(scalar) => match scalar {
                crate::ffi::contract::FfiScalarType::I32 => "int".to_string(),
                crate::ffi::contract::FfiScalarType::I64 => "long long".to_string(),
                crate::ffi::contract::FfiScalarType::Bool => "_Bool".to_string(),
            },
            crate::ffi::contract::FfiRetContract::Float => "double".to_string(),
            crate::ffi::contract::FfiRetContract::String
            | crate::ffi::contract::FfiRetContract::StringOwned => "char*".to_string(),
            crate::ffi::contract::FfiRetContract::RawPtr(_)
            | crate::ffi::contract::FfiRetContract::RawPtrMut(_) => "void*".to_string(),
            crate::ffi::contract::FfiRetContract::CShared(_)
            | crate::ffi::contract::FfiRetContract::CBorrow(_)
            | crate::ffi::contract::FfiRetContract::CBorrowMut(_) => "long long".to_string(),
            crate::ffi::contract::FfiRetContract::Json => "char*".to_string(),
            crate::ffi::contract::FfiRetContract::StructByValue(name) => format!("struct {}", name),
            crate::ffi::contract::FfiRetContract::Unsupported(_) => "void*".to_string(),
        }
    }

    fn mimi_type_to_go(&self, contract: &FfiContract, index: usize) -> String {
        if index >= contract.args.len() {
            return "unsafe.Pointer".to_string();
        }
        match &contract.args[index] {
            FfiArgContract::Int(scalar) => match scalar {
                crate::ffi::contract::FfiScalarType::I32 => "int32".to_string(),
                crate::ffi::contract::FfiScalarType::I64 => "int64".to_string(),
                crate::ffi::contract::FfiScalarType::Bool => "bool".to_string(),
            },
            FfiArgContract::Float => "float64".to_string(),
            FfiArgContract::StringBorrow
            | FfiArgContract::StringTransfer
            | FfiArgContract::Json => "string".to_string(),
            FfiArgContract::Cap(_) => "int64".to_string(),
            FfiArgContract::RawPtr(_) | FfiArgContract::RawPtrMut(_) => {
                "unsafe.Pointer".to_string()
            }
            FfiArgContract::CShared(_)
            | FfiArgContract::CBorrow(_)
            | FfiArgContract::CBorrowMut(_) => "int64".to_string(),
            FfiArgContract::StructByValue(name) => name.clone(),
            FfiArgContract::Callback {
                param_types,
                ret_type,
            } => self.callback_go_type(param_types, ret_type),
            FfiArgContract::Unsupported(_) => "unsafe.Pointer".to_string(),
        }
    }

    fn ret_type_to_go(&self, contract: &FfiContract) -> String {
        match &contract.ret {
            crate::ffi::contract::FfiRetContract::Unit => "".to_string(),
            crate::ffi::contract::FfiRetContract::Int(scalar) => match scalar {
                crate::ffi::contract::FfiScalarType::I32 => "int32".to_string(),
                crate::ffi::contract::FfiScalarType::I64 => "int64".to_string(),
                crate::ffi::contract::FfiScalarType::Bool => "bool".to_string(),
            },
            crate::ffi::contract::FfiRetContract::Float => "float64".to_string(),
            crate::ffi::contract::FfiRetContract::String
            | crate::ffi::contract::FfiRetContract::StringOwned => "string".to_string(),
            crate::ffi::contract::FfiRetContract::RawPtr(_)
            | crate::ffi::contract::FfiRetContract::RawPtrMut(_) => "unsafe.Pointer".to_string(),
            crate::ffi::contract::FfiRetContract::CShared(_)
            | crate::ffi::contract::FfiRetContract::CBorrow(_)
            | crate::ffi::contract::FfiRetContract::CBorrowMut(_) => "int64".to_string(),
            crate::ffi::contract::FfiRetContract::Json => "string".to_string(),
            crate::ffi::contract::FfiRetContract::StructByValue(name) => name.clone(),
            crate::ffi::contract::FfiRetContract::Unsupported(_) => "unsafe.Pointer".to_string(),
        }
    }

    fn mimi_type_to_c_field(&self, ty: &Type) -> String {
        match ty.unlocated() {
            Type::Name(name, _) => match name.as_str() {
                "i32" => "int".to_string(),
                "i64" => "long long".to_string(),
                "f64" => "double".to_string(),
                "bool" => "int".to_string(),
                other => {
                    if self.is_repr_c_record(other) {
                        format!("struct {}", other)
                    } else {
                        "void*".to_string()
                    }
                }
            },
            _ => "void*".to_string(),
        }
    }

    fn mimi_type_to_go_field(&self, ty: &Type) -> String {
        match ty.unlocated() {
            Type::Name(name, _) => match name.as_str() {
                "i32" => "int32".to_string(),
                "i64" => "int64".to_string(),
                "f64" => "float64".to_string(),
                "bool" => "bool".to_string(),
                other => {
                    if self.is_repr_c_record(other) {
                        other.to_string()
                    } else {
                        "unsafe.Pointer".to_string()
                    }
                }
            },
            _ => "unsafe.Pointer".to_string(),
        }
    }

    fn is_repr_c_record(&self, name: &str) -> bool {
        self.type_defs
            .get(name)
            .map(|td| {
                matches!(td.kind, TypeDefKind::Record(_))
                    && td.attributes.contains(&TypeAttribute::ReprC)
            })
            .unwrap_or(false)
    }
}

fn sanitize_go_package(name: &str) -> String {
    let mut s: String = name
        .chars()
        .map(|c| {
            if c.is_alphanumeric() || c == '_' {
                c
            } else {
                '_'
            }
        })
        .collect();
    if s.starts_with(|c: char| c.is_numeric()) {
        s = format!("_{}", s);
    }
    s.to_lowercase()
}

fn capitalize(s: &str) -> String {
    let mut chars = s.chars();
    match chars.next() {
        Some(c) => c.to_uppercase().to_string() + chars.as_str(),
        None => String::new(),
    }
}

fn go_export_name(s: &str) -> String {
    let mut chars = s.chars();
    match chars.next() {
        Some(c) => c.to_uppercase().to_string() + chars.as_str(),
        None => String::new(),
    }
}
